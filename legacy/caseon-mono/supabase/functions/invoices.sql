-- Setup cron job to mark overdue invoices daily
DO $$
BEGIN
    -- Check if job exists
    IF EXISTS (
        SELECT 1 
        FROM information_schema.tables 
        WHERE table_schema = 'cron' AND table_name = 'job'
    ) THEN
        -- Unschedule if already exists
        IF EXISTS (
            SELECT 1 
            FROM cron.job 
            WHERE jobname = 'mark_overdue_invoices_daily'
        ) THEN
            PERFORM cron.unschedule('mark_overdue_invoices_daily');
        END IF;
        
        -- Schedule daily job at 1 AM to mark overdue invoices
        PERFORM cron.schedule(
            'mark_overdue_invoices_daily',
            '0 1 * * *', -- Run at 1:00 AM every day
            'SELECT mark_overdue_invoices();'
        );
        
        RAISE NOTICE 'Setup daily job to mark overdue invoices at 1 AM';
    ELSE
        RAISE NOTICE 'Cron schema not found, skipping job creation for mark_overdue_invoices_daily';
    END IF;
END $$;
